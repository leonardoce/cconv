package it.interfree.leonardoce.iconv.math;

public class Ellisse2D 
{
	public double x, y, w, h;

	public Ellisse2D(double x, double y, double w, double h) {
		super();
		this.x = x;
		this.y = y;
		this.w = w;
		this.h = h;
	}
	
	public double getX()
	{
		return x;
	}
	
	public double getY()
	{
		return y;
	}
	
	public double getWidth()
	{
		return w;
	}
	
	public double getHeight()
	{
		return h;
	}
}
